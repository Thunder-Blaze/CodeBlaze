let age = prompt("What your age ");

// If Statement
if (age>0){
    console.log("This is a Valid Age");
} else {
    console.log("This is not a Valid Age");
}


// Switch Statement
switch (age){
    case 1:
        console.log("Your age is one");
    case 2:
        console.log("Your age is two");
        break;
    default:
        console.log("Have a Good Day");
}

